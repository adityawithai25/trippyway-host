/**
 * Get all images for a specific destination
 */
export function getLocalDestinationImages(destination: string): string[] {
  const dest = destination.toLowerCase();
  
  if (dest.includes('manali')) {
    return Object.values(LOCAL_DESTINATION_IMAGES.manali);
  }
  if (dest.includes('kasol')) {
    return Object.values(LOCAL_DESTINATION_IMAGES.kasol);
  }
  if (dest.includes('kalpa')) {
    return Object.values(LOCAL_DESTINATION_IMAGES.kalpa);
  }
  if (dest.includes('kullu')) {
    return Object.values(LOCAL_DESTINATION_IMAGES.kullu);
  }
  
  // Default: return all mountain images
  return [
    ...Object.values(LOCAL_DESTINATION_IMAGES.manali),
    ...Object.values(LOCAL_DESTINATION_IMAGES.kasol),
  ];
}

/**
 * Get images based on category
 */
export function getLocalCategoryImages(category: string): string[] {
  const cat = category.toLowerCase();
  
  if (cat.includes('couple')) {
    return LOCAL_CATEGORY_IMAGES.couples;
  }
  if (cat.includes('girl')) {
    return LOCAL_CATEGORY_IMAGES.girls;
  }
  if (cat.includes('boy')) {
    return LOCAL_CATEGORY_IMAGES.boys;
  }
  if (cat.includes('mixed')) {
    return LOCAL_CATEGORY_IMAGES.mixed;
  }
  if (cat.includes('family')) {
    return LOCAL_CATEGORY_IMAGES.family;
  }
  
  return LOCAL_CATEGORY_IMAGES.mixed;
}

/**
 * Get a main image based on destination and category
 */
export function getLocalMainImage(destination: string, category: string): string {
  const dest = destination.toLowerCase();
  const cat = category.toLowerCase();
  
  // Romantic/Couples packages
  if (cat.includes('couple') && dest.includes('manali')) {
    return LOCAL_DESTINATION_IMAGES.manali.romantic;
  }
  
  // Girls packages
  if (cat.includes('girl')) {
    if (dest.includes('kasol')) {
      return LOCAL_DESTINATION_IMAGES.kasol.houses;
    }
    return LOCAL_DESTINATION_IMAGES.kalpa.aerial;
  }
  
  // Boys packages
  if (cat.includes('boy')) {
    return LOCAL_DESTINATION_IMAGES.highAltitude.pass;
  }
  
  // Winter packages
  if (dest.includes('manali') && (dest.includes('winter') || dest.includes('snow'))) {
    return LOCAL_DESTINATION_IMAGES.manali.winter;
  }
  
  // Kasol packages
  if (dest.includes('kasol')) {
    return LOCAL_DESTINATION_IMAGES.kasol.valley;
  }
  
  // Default: Manali valley view
  return LOCAL_DESTINATION_IMAGES.manali.valley;
}

/**
 * Get diverse images for package gallery (5 images)
 */
export function getPackageGalleryImages(destination: string, category: string): string[] {
  const mainImage = getLocalMainImage(destination, category);
  const categoryImages = getLocalCategoryImages(category);
  const destImages = getLocalDestinationImages(destination);
  
  // Combine and ensure diversity
  const allImages = [mainImage, ...categoryImages, ...destImages];
  const uniqueImages = Array.from(new Set(allImages));
  
  // Return 5 unique images
  return uniqueImages.slice(0, 5);
}

/**
 * Get images for itinerary days
 */
export function getItineraryDayImages(destination: string, dayNumber: number): string[] {
  const dest = destination.toLowerCase();
  
  if (dest.includes('manali')) {
    const manaliImages = Object.values(LOCAL_DESTINATION_IMAGES.manali);
    // Rotate through images based on day number
    return [
      manaliImages[dayNumber % manaliImages.length],
      manaliImages[(dayNumber + 1) % manaliImages.length],
    ];
  }
  
  if (dest.includes('kasol')) {
    const kasolImages = Object.values(LOCAL_DESTINATION_IMAGES.kasol);
    return [
      kasolImages[dayNumber % kasolImages.length],
      kasolImages[(dayNumber + 1) % kasolImages.length],
    ];
  }
  
  // Mixed destination
  const allImages = [
    ...Object.values(LOCAL_DESTINATION_IMAGES.manali),
    ...Object.values(LOCAL_DESTINATION_IMAGES.kasol),
  ];
  
  return [
    allImages[dayNumber % allImages.length],
    allImages[(dayNumber + 2) % allImages.length],
  ];
}

